  * entity.addComponent('light', {
  *     type: "omni",
  *     color: new pc.Color(1, 0, 0),
  *     range: 10
  * });
  *
  * // Get the pc.LightComponent on an entity
  * var lightComponent = entity.light;
  *
  * // Update a property on a light component
  * entity.light.range = 20;
  * ```
